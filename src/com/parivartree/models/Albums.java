package com.parivartree.models;

public class Albums {
	
	private String mAlbumName;
	private String mAlbumHash;
	private String mPhotoHash;
	private String mFolderFlag;
	
	public String getmAlbumName() {
		return mAlbumName;
	}
	public void setmAlbumName(String mAlbumName) {
		this.mAlbumName = mAlbumName;
	}
	public String getmAlbumHash() {
		return mAlbumHash;
	}
	public void setmAlbumHash(String mAlbumHash) {
		this.mAlbumHash = mAlbumHash;
	}
	public String getmPhotoHash() {
		return mPhotoHash;
	}
	public void setmPhotoHash(String mPhotoHash) {
		this.mPhotoHash = mPhotoHash;
	}
	public String getmFolderFlag() {
		return mFolderFlag;
	}
	public void setmFolderFlag(String mFolderFlag) {
		this.mFolderFlag = mFolderFlag;
	}
	
	

}
